            return shippingValue;
        });

        // Ë®ÇÂñÆÁ∏ΩÈ°ç
        const grandTotal = computed(() => {
            return (Number(totalAmount.value) + shipping.value).toFixed(0);
        });

        // Ê∑ªÂä†Âà∞Ë≥ºÁâ©Ëªä
        const addItemToCartHandler = async (product) => {
            try {
                if (product.stockQuantity <= 0) {
